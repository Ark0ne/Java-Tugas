    private void selectInputCelcius(){
        if (inputCelciusRadioButton.isSelected() && outputCelciusRadioButton.isSelected()) {
            inputLabel.setText("Celcius");
            outputLabel.setText("Celcius");
            hasil = Double.parseDouble(textInput.getText());
            textOutput.setText(String.valueOf(String.format("%.2f",hasil)));
        } else if (inputCelciusRadioButton.isSelected() && outputFahrenheitRadioButton.isSelected()) {
            inputLabel.setText("Celcius");
            outputLabel.setText("Fahrenheit");
            hasil = (Double.parseDouble(textInput.getText()) * 1.8) + 32;
            textOutput.setText(String.valueOf(String.format("%.2f",hasil)));
        } else if (inputCelciusRadioButton.isSelected() && outputKelvinRadioButton.isSelected()) {
            inputLabel.setText("Celcius");
            outputLabel.setText("Kelvin");
            hasil = (Double.parseDouble(textInput.getText()) + 273.15);
            textOutput.setText(String.valueOf(String.format("%.2f",hasil)));
        } else if (inputCelciusRadioButton.isSelected() && outputReamurRadioButton.isSelected()) {
            inputLabel.setText("Celcius");
            outputLabel.setText("Reamur");
            hasil = (Double.parseDouble(textInput.getText()) * 0.8);
            textOutput.setText(String.valueOf(String.format("%.2f",hasil)));
        } 
    }
    
    private void selectInputFahrenheit(){
        if (inputFahrenheitRadioButton.isSelected() && outputCelciusRadioButton.isSelected()) {
            inputLabel.setText("Fahrenheit");
            outputLabel.setText("Celcius");
            hasil = (Double.parseDouble(textInput.getText()) - 32) / 1.8;
            textOutput.setText(String.valueOf(String.format("%.2f",hasil)));
        } else if (inputFahrenheitRadioButton.isSelected() && outputFahrenheitRadioButton.isSelected()) {
            inputLabel.setText("Fahrenheit");
            outputLabel.setText("Fahrenheit");
            hasil = Double.parseDouble(textInput.getText());
            textOutput.setText(String.valueOf(String.format("%.2f",hasil)));
        } else if (inputFahrenheitRadioButton.isSelected() && outputKelvinRadioButton.isSelected()) {
            inputLabel.setText("Fahrenheit");
            outputLabel.setText("Kelvin");
            hasil = (Double.parseDouble(textInput.getText()) + 459.67) / 1.8;
            textOutput.setText(String.valueOf(String.format("%.2f",hasil)));
        } else if (inputFahrenheitRadioButton.isSelected() && outputReamurRadioButton.isSelected()) {
            inputLabel.setText("Fahrenheit");
            outputLabel.setText("Reamur");
            hasil = (Double.parseDouble(textInput.getText()) - 32) / 2.25;
            textOutput.setText(String.valueOf(String.format("%.2f",hasil)));
        } 
    }
    
