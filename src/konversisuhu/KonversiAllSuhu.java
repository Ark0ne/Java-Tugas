    private void convertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertButtonActionPerformed
        // TODO add your handling code here:
        if ( (inputCelciusRadioButton.isSelected() || inputFahrenheitRadioButton.isSelected()
              || inputKelvinRadioButton.isSelected() || inputReamurRadioButton.isSelected())
              && (outputCelciusRadioButton.isSelected() || outputFahrenheitRadioButton.isSelected()
              || outputKelvinRadioButton.isSelected() || outputReamurRadioButton.isSelected()) ) {
            
            if (textInput.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "MASUKAN NILAI SUHU !!", "EROR" , JOptionPane.ERROR_MESSAGE);
            }else{
                selectInputCelcius();
                selectInputFahrenheit();
                selectInputKelvin();
                selectInputReamur();
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "PILIH SATUAN SUHU !!", "EROR" , JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_convertButtonActionPerformed

