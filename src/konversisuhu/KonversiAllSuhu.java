   private void selectInputReamur(){
       if (inputReamurRadioButton.isSelected() && outputCelciusRadioButton.isSelected()) {
           inputLabel.setText("Reamur");
           outputLabel.setText("Celcius");
           hasil = Double.parseDouble(textInput.getText()) / 0.8;
           textOutput.setText(String.valueOf(String.format("%.2f", hasil)));
       } else if (inputReamurRadioButton.isSelected() && outputFahrenheitRadioButton.isSelected()) {
           inputLabel.setText("Reamur");
           outputLabel.setText("Fahrenheit");
           hasil = (Double.parseDouble(textInput.getText()) - 7.5) * 24 / 7 +32;
           textOutput.setText(String.valueOf(String.format("%.2f", hasil)));
       } else if (inputReamurRadioButton.isSelected() && outputKelvinRadioButton.isSelected()) {
           inputLabel.setText("Reamur");
           outputLabel.setText("Kelvin");
           hasil = (Double.parseDouble(textInput.getText()) / 0.8) + 273.15;
           textOutput.setText(String.valueOf(String.format("%.2f", hasil)));
       } else if (inputReamurRadioButton.isSelected() && outputReamurRadioButton.isSelected()) {
           inputLabel.setText("Reamur");
           outputLabel.setText("Reamur");
           hasil = Double.parseDouble(textInput.getText());
           textOutput.setText(String.valueOf(String.format("%.2f", hasil)));
       } 
   } 
   
